let test = 5;

if (test > 10) {
	console.log('Переменная больше 10');
} else {
	console.log('Переменная меньше 10'); 
}

if (test < 10) {
	console.log('+++');
} else {
	console.log('---'); 
}

if (test >= 10) {
	console.log('Переменная больше или равна 10');
} else {
	console.log('Переменная меньше 10'); 
}

if (test <= 10) {
	console.log('Переменная меньше или равна 10');
} else {
	console.log('Переменная больше 10'); 
}